export default function decorate(b) {
  b.classList.add('footer-brand-section');
  const wrapper = b.querySelector('.footer-brand-wrapper');
  if (wrapper) wrapper.classList.add('footer-brand-wrapper');
  const primary = b.querySelector('.footer-brand-primary');
  if (primary) primary.classList.add('footer-brand-primary');
  const container = b.querySelectorAll('.footer-brand-container');
  container.forEach(c => c.classList.add('footer-brand-container'));
  const primaryContent = b.querySelector('.footer-brand-primary-content');
  if (primaryContent) primaryContent.classList.add('footer-brand-primary-content');
  const left = b.querySelectorAll('.footer-brand-left');
  left.forEach(l => l.classList.add('footer-brand-left'));
  const right = b.querySelectorAll('.footer-brand-right');
  right.forEach(r => r.classList.add('footer-brand-right'));
  const nav = b.querySelector('.footer-brand-navbar');
  if (nav) nav.classList.add('footer-brand-navbar');
  const navbarLeft = b.querySelector('.footer-brand-navbar-left');
  if (navbarLeft) navbarLeft.classList.add('footer-brand-navbar-left');
  const navbarRight = b.querySelector('.footer-brand-navbar-right');
  if (navbarRight) navbarRight.classList.add('footer-brand-navbar-right');
  const listComponents = b.querySelectorAll('.footer-list-component');
  listComponents.forEach(lc => lc.classList.add('footer-list-component'));
  const lists = b.querySelectorAll('.footer-list');
  lists.forEach(list => list.classList.add('footer-list'));
  const listItems = b.querySelectorAll('.footer-list-item');
  listItems.forEach(item => item.classList.add('footer-list-item'));
  const listLinks = b.querySelectorAll('.footer-list-item-link');
  listLinks.forEach(link => link.classList.add('footer-list-item-link'));
  const secondary = b.querySelector('.footer-brand-secondary');
  if (secondary) secondary.classList.add('footer-brand-secondary');
  const secondaryContent = b.querySelector('.footer-brand-secondary-content');
  if (secondaryContent) secondaryContent.classList.add('footer-brand-secondary-content');
  const socialTitle = b.querySelector('.footer-social-media-title');
  if (socialTitle) socialTitle.classList.add('footer-social-media-title');
  const socialList = b.querySelector('.footer-brand-right-list');
  if (socialList) socialList.classList.add('footer-brand-right-list');
  const socialItems = b.querySelectorAll('.footer-brand-right-item');
  socialItems.forEach(item => item.classList.add('footer-brand-right-item'));
  const socialLinks = b.querySelectorAll('.footer-brand-right-link');
  socialLinks.forEach(link => link.classList.add('footer-brand-right-link'));
  const leftList = b.querySelector('.footer-brand-left-list');
  if (leftList) leftList.classList.add('footer-brand-left-list');
  const leftItems = b.querySelectorAll('.footer-brand-left-item');
  leftItems.forEach(item => item.classList.add('footer-brand-left-item'));
  const leftLinks = b.querySelectorAll('.footer-brand-left-link');
  leftLinks.forEach(link => link.classList.add('footer-brand-left-link'));
  const copyright = b.querySelector('.footer-brand-left-copyright');
  if (copyright) copyright.classList.add('footer-brand-left-copyright');
}